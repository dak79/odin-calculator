/* Computational functions */

function add(a, b) {
    return a + b;
}

function substract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}


/* Test in console */
console.log(add(2, 2));

console.log(substract(3, 9));

console.log(multiply(3, 3));

console.log(divide(4, 2));
